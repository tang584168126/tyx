package class14;

public class Calculator {
	double result;
	//加的方法
	public void add(double num1, double num2){
		result = num1 + num2;
	}
	//减的方法
	public void minus(double num1, double num2){
		result = num1 - num2;
	}
	//乘的方法
	public void multiplde(double num1, double num2){
		result = num1 * num2;
	}
	//除的方法
	public void divide(double num1, double num2){
		result = num1 * num2;
	}

}
