package class2;

/**
 * 取模运算案例
 * @author fun
 *
 */
public class Test8 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		//   %  取模 (求余数) 通常只针对 整型
		//   /  求商 (直接相除得到结果)
		
		int i = 273 , b = 156 , c = 0 ; 
		
		c = i % b ;
			
		System.out.println("c = " + c);
		
		// 156 % 273 = 156
		// 273 % 156 = 117

	}

}





